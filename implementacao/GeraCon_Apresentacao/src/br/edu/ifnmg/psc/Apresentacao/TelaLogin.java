    private void extrairCampos() {
       String login = txtLogin.getText().trim();
       String senha = new String( txtSenha.getPassword());
        
        if( !login.equals("")){
            novo.setLogin(login);
        }
      
        if( !senha.equals("")){
             novo.setSenha(senha);
        }
    }

